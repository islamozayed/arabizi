#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn emoji_map_loads() {
        let map = build_emoji_map();
        assert!(map.len() > 50, "Emoji map should have 50+ entries");
    }

    #[test]
    fn common_arabic_words_have_emojis() {
        let map = build_emoji_map();
        assert!(map.contains_key("شكرًا"));
        assert!(map.contains_key("حبيبي"));
        assert!(map.contains_key("إن شاء الله"));
    }

    #[test]
    fn max_three_emojis() {
        let map = build_emoji_map();
        for (key, emojis) in &map {
            assert!(emojis.len() <= 3, "Key '{}' has {} emojis, max is 3", key, emojis.len());
        }
    }
}
